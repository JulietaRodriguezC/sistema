    public void mostrarDatos(){  //Para consultar y llenar la tabla
        while(modelo.getRowCount() > 0){  //Para que autom√°ticamente se deje de ver el registro borrado de la tabla en la interfaz
            modelo.removeRow(0);
        }
        
        conexion objConexion = new conexion();
        
        try {
            ResultSet resultado = objConexion.consultarRegistros("SELECT * FROM Empleados");
            while(resultado.next()){
                System.out.println(resultado.getString("ID"));
                System.out.println(resultado.getString("Nombre"));
                System.out.println(resultado.getString("Correo"));
                Object[] oUsuario = {resultado.getString("ID"),resultado.getString("Nombre"),resultado.getString("Correo")};
                modelo.addRow(oUsuario);
            }
        } catch (Exception e) {
            System.out.println(e);
        }
    }
    
    public empleadosBL recuperarDatosGUI(){  //Recuperar informaci√≤n de la interfaz gr√°fica
        empleadosBL oEmpleados = new empleadosBL();
        
        int ID = (txtid.getText().isEmpty())?0: Integer.parseInt(txtid.getText());
        oEmpleados.setID(ID);
        oEmpleados.setNombre(txtnombre.getText());
        oEmpleados.setCorreo(txtcorreo.getText());
        
        return oEmpleados;
    }
    
    public void limpiar(){
        txtid.setText("");
        txtnombre.setText("");
        txtcorreo.setText("");
        
        btnagregar.setEnabled(true);
        btneditar.setEnabled(false);
        btnborrar.setEnabled(false);
    }
